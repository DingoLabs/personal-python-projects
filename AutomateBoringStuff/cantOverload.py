def hello1():
    print('hi ')
    print('hello ')
    print('howdy ')
def hello1(name):
    print('hi ' + name)
    print('hello ' + name)
    print('howdy ' + name)
def hello1(name1, name2):
    print('hi ' + name1 + ' ' + name2)
    print('hello ' + name1 + ' ' + name2)
    print('howdy ' + name1 + ' ' + name2)
hello1()
hello1('Alice')
hello1('Alice','Dada')
